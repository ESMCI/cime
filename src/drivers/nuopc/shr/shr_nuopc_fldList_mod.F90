  use shr_kind_mod   , only : CX => shr_kind_CX, CXX => shr_kind_CXX, CS=>shr_kind_CS, CL=>shr_kind_CL
  use shr_sys_mod    , only : shr_sys_abort
  use shr_string_mod , only : shr_string_lastindex

